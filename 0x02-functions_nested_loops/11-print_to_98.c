#include <stdio.h>
#include "holberton.h"

/**
 * _abs returns the absolute value of an int
 */
void print_to_98(int n)
{
	if (n <= 98)
	{
		for (; n <= 98; n++)
		{
			printf("%d", n);
			if (n != 98)
				printf(", ");
		}
	}
	else
	{
		for (; n >= 98; n--)
		{
			printf("%d", n);
			if (n != 98)
				printf(", ");
		}
	}
	/* _putchar('\n'); */
	printf("\n");
}
